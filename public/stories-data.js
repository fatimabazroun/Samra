// ─── Helper queries ────────────────────────────────────────────────────────────

function getPopularStories() {
  return SAMRA_STORIES.filter(s => s.popular);
}

function getLibraryStories(library) {
  return SAMRA_STORIES.filter(s => s.library === library);
}

function getFeaturedStories(page) {
  return SAMRA_STORIES.filter(s => s.featured && s.homePage === page);
}

function getStoryById(id) {
  return SAMRA_STORIES.find(s => s.id === id) || null;
}
